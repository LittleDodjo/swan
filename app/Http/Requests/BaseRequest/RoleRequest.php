<?php

namespace App\Http\Requests\BaseRequest;

use App\Rules\BaseRule\RootRule;
use Illuminate\Foundation\Http\FormRequest;
use JetBrains\PhpStorm\ArrayShape;
use JetBrains\PhpStorm\Pure;

class RoleRequest extends FormRequest
{
    /**
     * Determine if the user is authorized to make this request.
     *
     * @return bool
     */
    public function authorize(): bool
    {
        return true;
    }

    /**
     * Get the validation rules that apply to the request.
     *
     * @return array<string, mixed>
     */
    #[ArrayShape(['password' => "array", 'is_confirmed' => "string[]", 'is_admin' => "string[]", 'is_root' => "string[]", 'incoming_manager' => "string", 'outgoing_manager' => "string"])]
    public function rules(): array
    {
        return [
            'password' => [
                'required',
                new RootRule,
            ],
            'is_confirmed' => [
                'boolean'
            ],
            'is_admin' => [
                'boolean'
            ],
            'is_root' => [
                'boolean'
            ],
            'incoming_manager' => 'boolean',
            'outgoing_manager' => 'boolean',
        ];
    }

    #[Pure] public function messages(): array
    {
        return parent::messages(); // TODO: Change the autogenerated stub
    }
}
